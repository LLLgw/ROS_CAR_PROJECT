//延时1us
static void OLED_BusDelay(void)
{
    delay_us(1);
}

//发送时序，软件SPI
static void OLED_WriteByte(uint8_t data, uint8_t type)
{
    uint8_t i;

	//判断数据标志
    if (type == OLED_DATA)
    {
        GPIO_SetBits(OLED_PORT, OLED_PIN_DC);					//发送数据DC置1
    }
    else
    {
        GPIO_ResetBits(OLED_PORT, OLED_PIN_DC);					//发送命令DC置0
    }

	//逐位发送8bit
    for (i = 0; i < 8; i++)
    {
        GPIO_ResetBits(OLED_PORT, OLED_PIN_SCL);				//拉低SCL，准备收

		//判断最高位
        if (data & 0x80)
        {
            GPIO_SetBits(OLED_PORT, OLED_PIN_SDA);				//最高位为1则SDA置1
        }
        else
        {
            GPIO_ResetBits(OLED_PORT, OLED_PIN_SDA);			//最高位为0则SDA置0
        }

        OLED_BusDelay();										//等待1us信号平稳
        GPIO_SetBits(OLED_PORT, OLED_PIN_SCL);					//拉高SCL，读
        OLED_BusDelay();

        data <<= 1;												//左移1位第2位变成最高位
    }
}

//发命令
static void OLED_WriteCmd(uint8_t cmd)
{
    OLED_WriteByte(cmd, OLED_CMD);
}

//发数据
static void OLED_WriteDataBlock(const uint8_t *data, uint16_t len)
{
    while (len--)
    {
        OLED_WriteByte(*data++, OLED_DATA);
    }
}

//OLED引脚初始化
static void OLED_GPIOInit(void)
{
    GPIO_InitTypeDef gpio;

    RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOD, ENABLE);

    gpio.GPIO_Pin = OLED_PIN_SCL | OLED_PIN_SDA | OLED_PIN_RES | OLED_PIN_DC;
    gpio.GPIO_Mode = GPIO_Mode_OUT;
    gpio.GPIO_OType = GPIO_OType_PP;
    gpio.GPIO_PuPd = GPIO_PuPd_NOPULL;
    gpio.GPIO_Speed = GPIO_Speed_2MHz;
    GPIO_Init(OLED_PORT, &gpio);

    GPIO_SetBits(OLED_PORT, OLED_PIN_SCL);
    GPIO_SetBits(OLED_PORT, OLED_PIN_SDA);
    GPIO_SetBits(OLED_PORT, OLED_PIN_RES);
    GPIO_SetBits(OLED_PORT, OLED_PIN_DC);
}

//设置光标位置
static void OLED_SetPos(uint8_t x, uint8_t page)
{
    OLED_WriteCmd((uint8_t)(0xB0 | page));						//设置页页地址
    OLED_WriteCmd((uint8_t)(0x10 | ((x >> 4) & 0x0F)));			//设置列地址高四位
    OLED_WriteCmd((uint8_t)(0x00 | (x & 0x0F)));				//设置低地址低四位
}

//字符ASCII转字库下标
static uint8_t OLED_GetCharIndex(char ch)
{
	//不在字库范围
    if ((ch < ' ') || (ch > '~'))
    {
        ch = '?';												//设为“？”表示未知
    }

    return (uint8_t)(ch - ' ');									//返回下标
}

//OLED初始化
void OLED_Init(void)
{
    OLED_GPIOInit();
    GPIO_ResetBits(OLED_PORT, OLED_PIN_RES);					//复位
    delay_ms(100);
    GPIO_SetBits(OLED_PORT, OLED_PIN_RES);						//释放复位

	//发命令配置OLED控制器
    OLED_WriteCmd(0xAE);
    OLED_WriteCmd(0x20);
    OLED_WriteCmd(0x02);
    OLED_WriteCmd(0x40);
    OLED_WriteCmd(0xA8);
    OLED_WriteCmd(0x3F);
    OLED_WriteCmd(0xD3);
    OLED_WriteCmd(0x00);
    OLED_WriteCmd(0xA1);
    OLED_WriteCmd(0xC8);
    OLED_WriteCmd(0xDA);
    OLED_WriteCmd(0x12);
    OLED_WriteCmd(0x81);
    OLED_WriteCmd(0x8F);
    OLED_WriteCmd(0xA4);
    OLED_WriteCmd(0xA6);
    OLED_WriteCmd(0xD5);
    OLED_WriteCmd(0x80);
    OLED_WriteCmd(0xD9);
    OLED_WriteCmd(0xF1);
    OLED_WriteCmd(0xDB);
    OLED_WriteCmd(0x40);
    OLED_WriteCmd(0x8D);
    OLED_WriteCmd(0x14);
    OLED_WriteCmd(0xAF);

    OLED_Clear();												//显示缓冲清零
    OLED_Refresh();												//刷新屏幕
}

//显示缓冲清零
void OLED_Clear(void)
{
    memset(oled_buffer, 0, sizeof(oled_buffer));				//1024字节全部填0
}

//刷新到屏幕
void OLED_Refresh(void)
{
    uint8_t page;

	//发送8页数据到OLED
    for (page = 0; page < OLED_PAGE_COUNT; page++)
    {
        OLED_SetPos(0, page);									//设置光标到页开头
        OLED_WriteDataBlock(oled_buffer[page], OLED_WIDTH);		//发送数据
    }
}

//显示字符串
void OLED_ShowString(uint8_t x,uint8_t y,const char *str)
{
    uint8_t i;
    uint8_t index;

	//显存越界或显存空指针判断
    if ((y >= OLED_PAGE_COUNT) || (str == 0))
    {
        return;
    }

	//字符串结尾或超出显存边界
    while ((*str != '\0') && (x < OLED_WIDTH))
    {
        index = OLED_GetCharIndex(*str);						//ASCII转换下标

        for (i = 0; (i < 6) && (x < OLED_WIDTH); i++)
        {
            oled_buffer[y][x++] = oled_font_6x8[index][i];		//将字符6字节拷贝到显存
        }

        str++;
    }
}

//显示整型
void OLED_ShowInt(uint8_t x,uint8_t y,uint32_t num,uint8_t len)
{
    char text[11];
    uint8_t i;

    if (len > 10)
    {
        len = 10;
    }

    text[len] = '\0';											//加上字符串结尾
    for (i = 0; i < len; i++)
    {
        text[len - 1 - i] = (char)('0' + (num % 10));			//取num个位转为char
        num /= 10;												//去掉个位
    }

    OLED_ShowString(x, y, text);								//显示字符串
}
